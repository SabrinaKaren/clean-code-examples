// Calcula o dobro de um número
function calcularDobro(num) {
    return num * 2; // multiplica o número por 2 para obter o dobro
}

// Verifica se um número é par
function verificarSeEPar(num) {
    return num % 2 === 0; // verifica se o número é divisível por 2
}

// Calcula a soma de dois números
function calcularSoma(a, b) {
    return a + b; // retorna a soma de a e b
}